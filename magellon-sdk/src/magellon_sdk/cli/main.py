def _iter_pack_files(source_dir: Path) -> Iterable[Path]:
    """Walk the source directory yielding files to include in the
    archive. Skips hidden dirs/files, build artifacts, and the
    source manifest itself (we write a regenerated one)."""
    for root, dirs, files in os.walk(source_dir):
        # In-place mutation of dirs[] tells os.walk what NOT to descend into.
        dirs[:] = [
            d for d in dirs
            if not d.startswith(".") and d not in SKIP_DIRS
        ]
        for name in files:
            if name.startswith("."):
                continue
            if name.endswith(SKIP_FILE_PATTERNS):
                continue
            # Skip the source manifest — pack writes a regenerated
            # version with stamped checksums + archive_id.
            if name in (CANONICAL_MANIFEST, LEGACY_MANIFEST):
                continue
            yield Path(root) / name


def _sha256(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def _warn_external_uv_sources(src: Path) -> None:
    """Warn if pyproject.toml's ``[tool.uv.sources]`` references paths
    that resolve outside the source directory.

    Such paths (e.g. ``path = "../../magellon-sdk"``) are common in
    workspace dev setups but break at install time because the
    archive is extracted into a different location. The bundled-wheel
    pattern (``path = "wheels/foo.whl"``) is the cross-host fix.

    Read-only via stdlib ``tomllib`` (Python 3.11+); on older Pythons
    or on parse errors we silently skip — the lint is a hint, not a
    gate.
    """
    pyproject = src / "pyproject.toml"
    if not pyproject.exists():
        return
    try:
        import tomllib  # type: ignore[import-not-found]
    except ImportError:
        return
    try:
        data = tomllib.loads(pyproject.read_text(encoding="utf-8"))
    except Exception:  # noqa: BLE001
        return
    sources = (
        data.get("tool", {}).get("uv", {}).get("sources", {})
    )
    if not isinstance(sources, dict):
        return
    bad: list[str] = []
    for name, spec in sources.items():
        if not isinstance(spec, dict):
            continue
        path_value = spec.get("path")
        if not path_value:
            continue
        # Resolve relative to the plugin source dir.
        resolved = (src / path_value).resolve()
        try:
            resolved.relative_to(src.resolve())
        except ValueError:
            bad.append(f"  {name} -> {path_value}  (resolves to {resolved})")
    if bad:
        sys.stdout.write(
            "warning: pyproject.toml has [tool.uv.sources] entries that "
            "resolve outside the plugin dir:\n"
            + "\n".join(bad) + "\n"
            + "  These paths will not exist after archive extraction. "
            "Bundle a wheel under wheels/ or pin via PyPI version "
            "instead.\n"
        )


def _stamp_manifest(
    manifest: PluginArchiveManifest, file_checksums: dict[str, str],
) -> PluginArchiveManifest:
    """Update the manifest with packed-archive metadata.

    - ``archive_id``: regenerate to a fresh UUID v7. The pack tool
      represents one *build*; subsequent rebuilds get new ids.
    - ``updated``: stamp now in UTC. ``created`` is preserved if
      already meaningful, otherwise also set to now.
    - ``file_checksums``: replace with the freshly computed map.
    """
    now = datetime.now(timezone.utc)
    payload = manifest.model_dump(mode="python")
    payload["archive_id"] = str(uuid7())
    payload["updated"] = now
    if not payload.get("created") or payload["created"] == manifest.updated:
        payload["created"] = now
    payload["file_checksums"] = file_checksums
    return PluginArchiveManifest.model_validate(payload)


def cmd_plugin_pack(args: argparse.Namespace) -> int:
    src = Path(args.dir).resolve()
    if not src.is_dir():
        sys.stderr.write(f"error: {src} is not a directory\n")
        return 1

    try:
        manifest, _src_path = _read_source_manifest(src)
    except Exception as exc:  # noqa: BLE001
        sys.stderr.write(f"error: {exc}\n")
        return 1

    # Lint pyproject.toml for [tool.uv.sources] paths that escape the
    # plugin dir. Such paths are common in dev (e.g. SDK at
    # ``../../magellon-sdk``) but break at install time because the
    # archive is unpacked into a different location. Warn loudly so
    # the author either bundles a wheel under ``wheels/`` or uses a
    # PyPI-style version pin.
    _warn_external_uv_sources(src)

    # Collect files to pack + their checksums. Paths in the archive
    # are POSIX-relative to the package root (which itself is the
    # source directory's name) — this is what the install controller
    # extracts back out.
    pack_files: list[tuple[Path, str]] = []
    file_checksums: dict[str, str] = {}
    for full in _iter_pack_files(src):
        arcname = full.relative_to(src).as_posix()
        digest = _sha256(full)
        pack_files.append((full, arcname))
        file_checksums[arcname] = digest

    # Stamp manifest with archive_id + updated + checksums.
    final_manifest = _stamp_manifest(manifest, file_checksums)
    manifest_yaml = dump_manifest_yaml(final_manifest)

    # Output path defaults to <plugin_id>-<version>.mpn next to source.
    out = Path(
        args.output
        or src.parent / f"{final_manifest.plugin_id}-{final_manifest.version}{ARCHIVE_EXT}"
    )
    if out.exists() and not args.force:
        sys.stderr.write(f"error: {out} already exists (pass --force to overwrite)\n")
        return 1

    with zipfile.ZipFile(out, "w", zipfile.ZIP_DEFLATED) as z:
        # Canonical manifest at the archive root.
        z.writestr(CANONICAL_MANIFEST, manifest_yaml)
        # Legacy alias — same content, alternate filename. Lets the
        # existing CoreService install controller (hard-coded to
        # plugin.yaml) read v1 archives until P4 retires it.
        z.writestr(LEGACY_MANIFEST, manifest_yaml)
        for full, arcname in pack_files:
            z.write(full, arcname=arcname)

    archive_size = out.stat().st_size
    archive_sha = _sha256(out)
    sys.stdout.write(
        f"Packed {final_manifest.plugin_id} v{final_manifest.version}\n"
        f"  archive: {out}\n"
        f"  size:    {archive_size:,} bytes\n"
        f"  sha256:  {archive_sha}\n"
        f"  files:   {len(pack_files)} (+ 2 manifest aliases)\n"
    )
    return 0


