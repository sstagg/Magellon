"""``magellon-sdk`` CLI dispatcher.

Three subcommands today, all under the ``plugin`` group:

    magellon-sdk plugin init <name>     scaffold a new plugin directory
    magellon-sdk plugin pack <dir>      validate manifest + zip into a .mpn
    magellon-sdk plugin validate <p>    validate a manifest (dir, file, or .mpn)

Hub-side commands (``hub login``, ``hub publish``, ``hub install``)
land later under the ``hub`` group when the hub itself is deployed.

Naming notes
============

- Canonical archive extension: ``.mpn`` ("Magellon plugin"). ``.zip``
  is also accepted on read paths since the underlying archive is a
  zip; ``pack`` only emits ``.mpn``.
- Canonical manifest filename inside the archive: ``manifest.yaml``.
  ``pack`` also writes a copy at the legacy name ``plugin.yaml``
  so the existing CoreService install controller (which hard-codes
  ``plugin.yaml``) keeps working until P4 lands.
"""
from __future__ import annotations

import argparse
import hashlib
import os
import sys
import zipfile
from datetime import datetime, timezone
from pathlib import Path
from typing import Iterable, List, Optional, Tuple

from magellon_sdk import __version__
from magellon_sdk.archive.manifest import (
    PluginArchiveManifest,
    SUPPORTED_MANIFEST_VERSIONS,
    check_sdk_compat,
    dump_manifest_yaml,
    load_manifest_bytes,
    uuid7,
)
from magellon_sdk.archive.manifest import _parse_version  # type: ignore[attr-defined]


CANONICAL_MANIFEST = "manifest.yaml"
LEGACY_MANIFEST = "plugin.yaml"
ARCHIVE_EXT = ".mpn"
LEGACY_ARCHIVE_EXTS = (".zip",)
SKIP_DIRS = {"__pycache__", ".git", ".venv", "venv", "node_modules", ".pytest_cache"}
SKIP_FILE_PATTERNS = (".pyc",)


# ---------------------------------------------------------------------------
# Manifest loading — accepts canonical OR legacy name from a source directory
# ---------------------------------------------------------------------------

def _read_source_manifest(source_dir: Path) -> tuple[PluginArchiveManifest, Path]:
    """Find and load the manifest from a plugin source directory.

    Prefers ``manifest.yaml``; falls back to ``plugin.yaml`` so v0
    plugin layouts still pack. Returns the parsed manifest and the
    path it came from."""
    canonical = source_dir / CANONICAL_MANIFEST
    legacy = source_dir / LEGACY_MANIFEST
    if canonical.exists():
        return load_manifest_bytes(canonical.read_bytes()), canonical
    if legacy.exists():
        return load_manifest_bytes(legacy.read_bytes()), legacy
    raise FileNotFoundError(
        f"{source_dir} has no {CANONICAL_MANIFEST} or {LEGACY_MANIFEST} — "
        f"did you mean `magellon-sdk plugin init`?"
    )


def _load_manifest_from_path(path: Path) -> PluginArchiveManifest:
    """Used by ``validate`` — accepts a directory, a manifest file, or
    an archive (``.mpn`` / ``.zip``)."""
    if path.is_dir():
        return _read_source_manifest(path)[0]
    if path.suffix in (ARCHIVE_EXT,) + LEGACY_ARCHIVE_EXTS:
        with zipfile.ZipFile(path) as z:
            for name in (CANONICAL_MANIFEST, LEGACY_MANIFEST):
                try:
                    with z.open(name) as f:
                        return load_manifest_bytes(f.read())
                except KeyError:
                    continue
        raise FileNotFoundError(
            f"{path} is a zip but contains neither "
            f"{CANONICAL_MANIFEST} nor {LEGACY_MANIFEST}"
        )
    if path.is_file():
        return load_manifest_bytes(path.read_bytes())
    raise FileNotFoundError(f"not found: {path}")


