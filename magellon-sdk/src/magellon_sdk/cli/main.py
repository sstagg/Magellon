def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="magellon-sdk",
        description="Magellon Plugin SDK — author tooling for .mpn archives.",
    )
    parser.add_argument(
        "--version",
        action="version",
        version=f"magellon-sdk {__version__}",
    )

    sub = parser.add_subparsers(dest="group", required=False, metavar="GROUP")

    # plugin group
    plugin = sub.add_parser("plugin", help="Plugin archive author tooling.")
    plugin_sub = plugin.add_subparsers(
        dest="command", required=True, metavar="COMMAND",
    )

    p_init = plugin_sub.add_parser(
        "init", help="Scaffold a new plugin archive directory.",
    )
    p_init.add_argument("name", help="Directory to create for the new plugin.")
    p_init.set_defaults(func=cmd_plugin_init)

    p_pack = plugin_sub.add_parser(
        "pack",
        help=f"Validate {CANONICAL_MANIFEST} and zip the directory to {ARCHIVE_EXT}.",
    )
    p_pack.add_argument("dir", help="Plugin directory containing manifest.yaml.")
    p_pack.add_argument(
        "-o", "--output",
        help=f"Output archive path. Defaults to "
             f"<plugin_id>-<version>{ARCHIVE_EXT} alongside the source dir.",
    )
    p_pack.add_argument(
        "--force", action="store_true",
        help="Overwrite an existing output archive.",
    )
    p_pack.set_defaults(func=cmd_plugin_pack)

    p_val = plugin_sub.add_parser(
        "validate",
        help=f"Validate a plugin manifest (dir / {CANONICAL_MANIFEST} / {ARCHIVE_EXT}).",
    )
    p_val.add_argument("path")
    p_val.set_defaults(func=cmd_plugin_validate)

    p_lint = plugin_sub.add_parser(
        "lint",
        help="Deeper checks than validate — install method completeness, "
             "bundled wheel staleness, health-check sanity, polish hints.",
    )
    p_lint.add_argument("dir", help="Plugin directory.")
    p_lint.add_argument(
        "--strict", action="store_true",
        help="Treat warnings + infos as failures (CI gate flag).",
    )
    p_lint.set_defaults(func=cmd_plugin_lint)

    p_scaffold = plugin_sub.add_parser(
        "scaffold",
        help="Generate a runnable plugin skeleton (manifest, main.py, "
             "plugin/, Dockerfile, pyproject, tests).",
    )
    p_scaffold.add_argument("name", help="Directory to create.")
    p_scaffold.add_argument(
        "--category", required=True,
        help="TaskCategory the plugin serves (e.g. 'fft', 'ctf').",
    )
    p_scaffold.add_argument(
        "--plugin-id",
        help="Explicit plugin_id; defaults to the directory name lowercased.",
    )
    p_scaffold.add_argument(
        "--display-name",
        help="Human-readable name for manifest; defaults to the directory name.",
    )
    p_scaffold.set_defaults(func=cmd_plugin_scaffold)

    p_test = plugin_sub.add_parser(
        "test",
        help="Run the author's pre-pack checks: lint + pytest "
             "+ optional /health probe.",
    )
    p_test.add_argument("dir", help="Plugin directory.")
    p_test.add_argument(
        "--http",
        help="Probe this URL's /health before running pytest "
             "(e.g. http://127.0.0.1:8000). Optional.",
    )
    p_test.set_defaults(func=cmd_plugin_test)

    return parser


def main(argv: Optional[List[str]] = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    if not getattr(args, "func", None):
        # Bare ``magellon-sdk`` with no subcommand — print help, exit 0.
        parser.print_help()
        return 0
    return args.func(args)


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main())
